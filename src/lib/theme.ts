export const setColorPokemon = (color: any) => {
  if (color === "grass") {
    return "#4CAF50";
  } else if (color === "poison") {
    return "#00b8a9";
  } else if (color === "water") {
    return "#2196F3";
  } else if (color === "normal") {
    return "#BDBDBD";
  } else if (color === "fighting") {
    return "#8D6E63";
  } else if (color === "flying") {
    return "#81C784";
  } else if (color === "ground") {
    return "#795548";
  } else if (color === "rock") {
    return "#9E9E9E";
  } else if (color === "steel") {
    return "#757575";
  } else if (color === "fire") {
    return "#F44336";
  } else if (color === "electric") {
    return "#ffbd39";
  } else if (color === "psychic") {
    return "#f08a5d";
  } else if (color === "ice") {
    return "#B3E5FC";
  } else if (color === "dragon") {
    return "#6200EA";
  } else if (color === "dark") {
    return "#263238";
  } else if (color === "fairy") {
    return "#E91E63";
  } else if (color === "shadow") {
    return "#000000";
  } else if (color === "unknown") {
    return "#9C27B0";
  } else {
    return "#32a852";
  }
};
